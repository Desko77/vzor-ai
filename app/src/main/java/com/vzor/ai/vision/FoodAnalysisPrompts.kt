package com.vzor.ai.vision

/**
 * Промпты для анализа еды/калорийности (UC#4).
 *
 * Используется VisionRouter и ToolRegistry для запросов типа
 * "что это за блюдо?", "сколько тут калорий?", "из чего это приготовлено?".
 */
object FoodAnalysisPrompts {

    /** Проверяет, является ли запрос пользователя запросом об еде. */
    fun isFoodQuery(prompt: String): Boolean {
        val lower = prompt.lowercase()
        return FOOD_KEYWORDS.any { lower.contains(it) }
    }

    /** Основной промпт для анализа блюда. */
    fun buildAnalysisPrompt(userQuery: String): String = """
        |Ты — эксперт-диетолог. Проанализируй фотографию еды.
        |
        |Запрос пользователя: <user_request>$userQuery</user_request>
        |
        |Ответь в следующем формате:
        |🍽 Блюдо: [название блюда]
        |📋 Ингредиенты: [основные ингредиенты через запятую]
        |🔥 Калории: [примерная калорийность порции] ккал
        |⚖️ БЖУ: Б [г] / Ж [г] / У [г]
        |💡 Комментарий: [краткий совет или факт о блюде]
        |
        |Если на фотографии несколько блюд, перечисли каждое.
        |Если это не еда, скажи что видишь и предложи сфотографировать еду.
        |Отвечай на русском языке, кратко.
    """.trimMargin()

    /** Промпт для быстрой оценки калорийности (без детализации). */
    fun buildQuickCaloriePrompt(): String = """
        |Определи блюдо на фото и оцени примерную калорийность порции.
        |Ответь одной строкой: "[Блюдо] — ~[число] ккал".
        |Если несколько блюд, перечисли каждое. Отвечай на русском.
    """.trimMargin()

    /** Промпт для анализа состава (аллергены, глютен и т.д.). */
    fun buildIngredientsPrompt(): String = """
        |Определи блюдо на фотографии и перечисли вероятные ингредиенты.
        |Отметь потенциальные аллергены: глютен, молоко, орехи, яйца, морепродукты.
        |Формат: "Блюдо: [...]. Ингредиенты: [...]. Аллергены: [...] (или нет)."
        |Отвечай на русском, кратко.
    """.trimMargin()

    private val FOOD_KEYWORDS = listOf(
        "калори", "ккал", "калорийность",
        "блюдо", "еда", "пища",
        "что за блюдо", "что это за еда",
        "бжу", "белки", "жиры", "углеводы",
        "ингредиент", "состав",
        "аллерген", "глютен",
        "диет", "порция",
        "сколько калорий", "калорий тут",
        "из чего", "приготовлено",
        "рецепт"
    )
}
