package com.vzor.ai.vision

/**
 * Помощник для шопинга (UC#5): сравнение товаров, чтение ценников,
 * поиск отзывов и альтернатив.
 *
 * Работает через VisionRouter (анализ изображения) + web.search (цены/отзывы).
 */
object ShoppingComparisonHelper {

    /** Проверяет, является ли запрос шопинг-запросом. */
    fun isShoppingQuery(prompt: String): Boolean {
        val lower = prompt.lowercase()
        return SHOPPING_KEYWORDS.any { lower.contains(it) }
    }

    /** Промпт для анализа товара на полке / в руках. */
    fun buildProductAnalysisPrompt(userQuery: String): String = """
        |Ты — помощник по покупкам. Проанализируй товар на фотографии.
        |
        |Запрос пользователя: <user_request>$userQuery</user_request>
        |
        |Определи:
        |📦 Товар: [название, бренд]
        |💰 Цена: [если видна на ценнике]
        |📊 Характеристики: [вес/объём, основные параметры]
        |⭐ Рекомендация: [стоит ли покупать, на что обратить внимание]
        |
        |Если видишь ценник — прочитай его точно.
        |Если видишь несколько товаров — сравни их кратко.
        |Отвечай на русском языке, кратко и по делу.
    """.trimMargin()

    /** Промпт для сравнения двух товаров. */
    fun buildComparisonPrompt(): String = """
        |На фотографии два или более товара. Сравни их по:
        |1. Цена (если видна)
        |2. Состав/характеристики
        |3. Соотношение цена/качество
        |Дай рекомендацию какой лучше выбрать. Отвечай на русском, кратко.
    """.trimMargin()

    /** Промпт для чтения ценника/этикетки. */
    fun buildPriceTagPrompt(): String = """
        |Прочитай ценник или этикетку на фотографии.
        |Укажи: название товара, цену, вес/объём, цену за единицу (если есть).
        |Если есть акция/скидка — отметь это.
        |Отвечай на русском, точно и кратко.
    """.trimMargin()

    /**
     * Формирует запрос для веб-поиска цен/отзывов.
     * @return Поисковый запрос для Tavily/Google.
     */
    fun buildWebSearchQuery(productName: String): String {
        return "$productName цена отзывы купить"
    }

    /**
     * Формирует запрос для поиска альтернатив.
     */
    fun buildAlternativesQuery(productName: String): String {
        return "$productName аналоги альтернативы сравнение"
    }

    private val SHOPPING_KEYWORDS = listOf(
        "сколько стоит", "цена", "ценник",
        "купить", "покупка", "покупать",
        "товар", "продукт",
        "сравни", "сравнение", "сравнить",
        "что лучше", "какой лучше",
        "отзыв", "рейтинг",
        "акция", "скидка", "распродажа",
        "этикетк", "состав", "штрихкод",
        "альтернатив", "аналог",
        "магазин", "полка"
    )
}
