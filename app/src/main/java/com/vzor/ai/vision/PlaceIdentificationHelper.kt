package com.vzor.ai.vision

/**
 * Помощник по идентификации мест (UC#2).
 *
 * Распознаёт достопримечательности, здания, заведения через VLM + web search.
 * Pipeline: камера → VLM (описание) → web.search (детали) → TTS (ответ).
 */
object PlaceIdentificationHelper {

    /** Проверяет, является ли запрос запросом о месте/здании. */
    fun isPlaceQuery(prompt: String): Boolean {
        val lower = prompt.lowercase()
        return PLACE_KEYWORDS.any { lower.contains(it) }
    }

    /** Промпт для идентификации здания/места. */
    fun buildPlaceIdentificationPrompt(userQuery: String): String = """
        |Определи здание, место или достопримечательность на фотографии.
        |
        |Запрос пользователя: $userQuery
        |
        |Укажи:
        |🏛 Название: [название места]
        |📍 Тип: [музей/ресторан/магазин/памятник/парк/и т.д.]
        |📝 Описание: [1-2 предложения]
        |⏰ Режим работы: [если можешь определить тип заведения]
        |
        |Если не можешь точно определить — опиши стиль здания и предположи тип.
        |Отвечай на русском, кратко.
    """.trimMargin()

    /** Промпт для описания достопримечательности. */
    fun buildLandmarkPrompt(): String = """
        |Определи достопримечательность на фотографии.
        |Если узнаёшь — назови, дай 1-2 факта.
        |Если не узнаёшь — опиши архитектурный стиль, примерную эпоху, тип сооружения.
        |Отвечай на русском, кратко.
    """.trimMargin()

    /**
     * Формирует поисковый запрос для получения доп. информации о месте.
     */
    fun buildWebSearchQuery(placeName: String): String {
        return "$placeName часы работы отзывы информация"
    }

    /**
     * Формирует запрос для поиска ближайших мест (POI).
     */
    fun buildNearbyQuery(placeType: String, location: String? = null): String {
        return if (location != null) {
            "$placeType рядом $location"
        } else {
            "$placeType рядом со мной"
        }
    }

    private val PLACE_KEYWORDS = listOf(
        "что за здание",
        "что за место",
        "что это за место",
        "где я нахожусь",
        "где мы",
        "что за дом",
        "какой это",
        "достопримечательност",
        "памятник",
        "что за магазин",
        "что за ресторан",
        "что за кафе",
        "что это за улица",
        "название здания",
        "какое здание"
    )
}
