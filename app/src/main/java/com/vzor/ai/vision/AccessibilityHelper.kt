package com.vzor.ai.vision

/**
 * Помощник по доступности (UC#8: Be My Eyes).
 *
 * Анализирует сцену для слабовидящих:
 * - Описание окружения (предметы, люди, пространство)
 * - Чтение текста (вывески, меню, документы)
 * - Навигационные подсказки (препятствия, двери, ступеньки)
 * - Идентификация объектов (продукты, лекарства, купюры)
 */
object AccessibilityHelper {

    /** Проверяет, является ли запрос запросом о доступности. */
    fun isAccessibilityQuery(prompt: String): Boolean {
        val lower = prompt.lowercase()
        return ACCESSIBILITY_KEYWORDS.any { lower.contains(it) }
    }

    /** Промпт для общего описания окружения (для слабовидящих). */
    fun buildSceneDescriptionPrompt(): String = """
        |Опиши сцену для слабовидящего человека. Важные детали:
        |1. Общая обстановка (помещение/улица, освещение)
        |2. Люди рядом (количество, расстояние, направление)
        |3. Препятствия (ступеньки, пороги, столбы, двери)
        |4. Текст (вывески, указатели, таблички)
        |5. Важные объекты (светофор, переход, транспорт)
        |
        |Говори чётко и кратко. Используй направления: слева, справа, впереди, сзади.
        |Начни с самого важного. Отвечай на русском.
    """.trimMargin()

    /** Промпт для чтения текста вслух (вывески, документы и т.д.). */
    fun buildReadAloudPrompt(): String = """
        |Прочитай весь текст на фотографии. Читай точно, ничего не пропуская.
        |Если текст на нескольких строках — читай по порядку.
        |Если есть заголовки — выдели их. Укажи язык текста.
        |Отвечай на русском.
    """.trimMargin()

    /** Промпт для навигационных подсказок. */
    fun buildNavigationAssistPrompt(): String = """
        |Помоги слабовидящему с навигацией. Опиши:
        |1. Что впереди (дорога, тротуар, пересечение)
        |2. Препятствия (ступеньки вверх/вниз, бордюр, яма)
        |3. Двери и входы (открыты/закрыты, направление открытия)
        |4. Светофор (если есть — цвет)
        |5. Безопасно ли идти вперёд
        |
        |Будь предельно конкретен. "2 ступеньки вниз через 3 метра" лучше чем "лестница впереди".
        |Отвечай на русском.
    """.trimMargin()

    /** Промпт для идентификации предмета в руке. */
    fun buildObjectIdentificationPrompt(userQuery: String): String = """
        |Человек с нарушением зрения держит предмет или указывает на него.
        |Запрос: $userQuery
        |
        |Определи:
        |1. Что это за предмет
        |2. Если продукт — название, бренд, срок годности (если видно)
        |3. Если лекарство — название, дозировка
        |4. Если купюра — номинал и валюта
        |5. Если документ — тип и ключевые данные
        |
        |Отвечай чётко и кратко, на русском.
    """.trimMargin()

    private val ACCESSIBILITY_KEYWORDS = listOf(
        "что вокруг меня",
        "опиши окружение",
        "помоги пройти",
        "помоги перейти",
        "что впереди",
        "есть ли препятстви",
        "прочитай вслух",
        "прочитай всё",
        "прочитай для меня",
        "что я держу",
        "что в руке",
        "что за предмет",
        "помоги увидеть",
        "помоги разглядеть",
        "безопасно ли",
        "куда идти",
        "где дверь",
        "где выход"
    )
}
